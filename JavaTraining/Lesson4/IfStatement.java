public class IfStatement {
  public static void main(String[] args) {
    int num=4;
    if(num == 1) {
      System.out.println("numの値は1です。");
    } else if(num == 2){
      System.out.println("numの値は2です。");
    } else if(num == 3){
      System.out.println("numの値は3です。");
    } else {
      System.out.println("numの値は1,2,3以外の値です。");
    }
  }
}
